                <h3>IL PAESE</h3>
                <div id="testo">
                    <p><a id="first2">B</a>ortigali è un comune italiano di 1357 abitanti della provincia di Nuoro, 
                       in Sardegna. Si trova nella subregione storica del Marghine.
                       Collocato ad anfiteatro ai piedi del trachitico monte Santu Padre, Bortigali
                       è posto ad un'altitudine media di 510 metri. Si trova nelle vicinanze della strada
                       statale 131 e per questa caratteristica è facilmente  
                       raggiungibile da tutte le parti della Sardegna. <div id="slideshow">
	               <img src="../immagini/neve.jpg"/>
	               <img src="../immagini/paese26.jpg"/>
	               <img src="../immagini/scorcio.jpg"/>
	               <img src="../immagini/orolo.jpg"/>
                       </div>È uno dei pochi paesi in Sardegna ad avere un mulino ad acqua funzionante 
                       anche se non utilizzato. Bortigali fa parte dell'Unione dei comuni del Marghine e del GAL 
                       Marghine. Dal 2012 è entrato a far parte della rete dei Borghi autentici d'Italia,
                       una rete nazionale alla quale aderiscono oltre 200 piccoli e medi comuni, enti territoriali
                       ed organismi misti di sviluppo locale. </br>
                       Uno tra gli eventi principali che riguardarono il nostro paese avenne durante la seconda guerra mondiale.
                       Bortigali divenne, durante la seconda guerra mondiale, il punto di riferimento di tutta 
                       la Sardegna, dopo che il generale Antonio Basso la scelse (forse per la sua posizione centrale 
                       e per la protezione del monte Santu Padre) come sede del Comando supremo delle forze armate 
                       della Sardegna. Nel 1943, quando gli americani fecero credere ad un loro sbarco in forze 
                       nell'isola, la sua importanza valicò i confini strettamente regionali. Nei giorni successivi 
                       all'armistizio dell'8 settembre, grazie all'intuizione e all'opera di alcuni ufficiali e 
                       soldati addetti alle stazioni radio (tra le più potenti del periodo), furono mandate in onda, 
                       in Sardegna e in tutto il mondo, delle brevi trasmissioni (che, nel primo periodo, avevano uno 
                       scopo "umanitario": inviare al di fuori dell'isola le notizie di quanti, militari e civili, 
                       vi risiedevano), precedute dalle prime note dell'Inno Sardo e dalla sigla 
                       <i>«Qui Radio Sardegna, libera voce d'Italia…»</i>.  
                       Nel 1945, trasferitasi a Cagliari, diede in anteprima mondiale <b>(venti minuti prima di 
                       Radio Londra)</b> l'annuncio della fine della guerra. Il 40º anniversario della prima radio 
                       libera sarda fu festeggiato alla presenza dei suoi pionieri il 16 ottobre 1983 con 
                       ospiti alcune importanti autorità dello Stato (fra le quali il presidente del Senato 
                       Francesco Cossiga, il ministro della Marina mercantile Ariuccio Carta e il presidente 
                       della Regione Angelo Rojch).</p>
