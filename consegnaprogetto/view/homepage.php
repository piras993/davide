                    febbraio 2002 presso l’Ufficio del Registro di Macomer. Essa ha come scopo la ricerca
                    e l’approfondimento di tutto ciò che attiene alla <b>cultura tradizionale di Bortigali</b>, 
                    con particolare riguardo <i>al canto a chidasantinu, a cuncordu e alle poesie dei poeti del luogo.</i>
                    Per statuto, dunque, l’Associazione Sas Enas,
                    che non ha scopo di lucro, si propone in primo luogo
                    di recuperare e valorizzare i canti tradizionali di Bortigali, ma anche di promuovere e favorire 
                    incisioni discografiche e pubblicazioni editoriali, di organizzare pubbliche esecuzioni, rassegne
                    e conferenze, mostre e quanto altro possa essere utile per la conoscenza della cultura della 
                    Sardegna e degli altri popoli del mondo, con un particolare riguardo alle cosiddette minoranze 
                    linguistiche.<br/>
